
public class Calculadora {
	
	public double somar() {
		return 0;
		
	}
	
	public double subtrair(int x, int y) {
		return x+y;
		
	}
	
	public double multiplicar(int x, int y) {
		return x*y;
		
	}
	
	public double dividir(int x, int y) {
		return x/y;
		
	}
	
	public double dividirUmPor(int x) {
		return 1/x;
		
	}
	
	public double elevarPorDois(int x) {
		return x*x;
		
	}
	
	public double raizDe(int x) {
		return Math.sqrt(x);
		
	}
	
	
	
	
}
